package com.bacode.hi.vo;

public class BarcodeVO {

	private String bar_code;
	private String ma_code;
	private String bar_exp;
	private String bar_lot;
	private String bar_serial;
	
	
	public String getBar_code() {
		return bar_code;
	}
	public void setBar_code(String bar_code) {
		this.bar_code = bar_code;
	}
	public String getMa_code() {
		return ma_code;
	}
	public void setMa_code(String ma_code) {
		this.ma_code = ma_code;
	}
	public String getBar_exp() {
		return bar_exp;
	}
	public void setBar_exp(String bar_exp) {
		this.bar_exp = bar_exp;
	}
	public String getBar_lot() {
		return bar_lot;
	}
	public void setBar_lot(String bar_lot) {
		this.bar_lot = bar_lot;
	}
	public String getBar_serial() {
		return bar_serial;
	}
	public void setBar_serial(String bar_serial) {
		this.bar_serial = bar_serial;
	}
}
